def clear():
    if os.name == 'posix': # Unix/Linux/MacOS
        os.system('clear')
    elif os.name == 'nt': # Windows
        os.system('cls')
    else:
        print("Unsupported operating system")
        raise SystemExit
    
def pause(text: str = None):
    if text: print(text)
    if os.name == 'posix': # Unix/Linux/MacOS
        os.system('read -n 1 -s -r -p ""')
    elif os.name == 'nt': # Windows
        os.system('pause >nul')
    else:
        print("Unsupported operating system")
        raise SystemExit